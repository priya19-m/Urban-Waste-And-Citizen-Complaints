package com.waste.bean;

public class Citizen {
	private String citizenID;
	private String fullName;
	private String houseOrBuildingNo;
	private String streetName;
	private String areaOrLocality;
	private String wardCode;
	private String routeCode;
	private String mobile;
	private String email;
	private String status;
	public String getCitizenID() {
		return citizenID;
	}
	public void setCitizenID(String citizenID) {
		this.citizenID = citizenID;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getHouseOrBuildingNo() {
		return houseOrBuildingNo;
	}
	public void setHouseOrBuildingNo(String houseOrBuildingNo) {
		this.houseOrBuildingNo = houseOrBuildingNo;
	}
	public String getStreetName() {
		return streetName;
	}
	public void setStreetName(String streetName) {
		this.streetName = streetName;
	}
	public String getAreaOrLocality() {
		return areaOrLocality;
	}
	public void setAreaOrLocality(String areaOrLocality) {
		this.areaOrLocality = areaOrLocality;
	}
	public String getWardCode() {
		return wardCode;
	}
	public void setWardCode(String wardCode) {
		this.wardCode = wardCode;
	}
	public String getRouteCode() {
		return routeCode;
	}
	public void setRouteCode(String routeCode) {
		this.routeCode = routeCode;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
